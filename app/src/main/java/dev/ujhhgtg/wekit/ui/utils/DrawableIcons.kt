package dev.ujhhgtg.wekit.ui.utils

import android.graphics.Canvas
import android.graphics.Color
import android.graphics.ColorFilter
import android.graphics.Paint
import android.graphics.PixelFormat
import android.graphics.drawable.Drawable
import androidx.core.graphics.PathParser
import androidx.core.graphics.withSave

abstract class VectorPathDrawable(
    pathData: String
) : Drawable() {

    protected val path = PathParser.createPathFromPathData(pathData)

    protected val paint = Paint(Paint.ANTI_ALIAS_FLAG).apply {
        style = Paint.Style.FILL
        color = Color.WHITE
    }

    override fun draw(canvas: Canvas) {
        val bounds = bounds

        canvas.withSave {
            val scaleX = bounds.width() / 960f
            val scaleY = bounds.height() / 960f

            translate(bounds.left.toFloat(), bounds.top.toFloat())
            scale(scaleX, scaleY)

            drawPath(path, paint)
        }
    }

    override fun setAlpha(alpha: Int) {
        paint.alpha = alpha
        invalidateSelf()
    }

    override fun setColorFilter(colorFilter: ColorFilter?) {
        paint.colorFilter = colorFilter
        invalidateSelf()
    }

    override fun getIntrinsicWidth(): Int = 24.toDp()

    override fun getIntrinsicHeight(): Int = 24.toDp()

    override fun isAutoMirrored(): Boolean = true

    override fun onLayoutDirectionChanged(layoutDirection: Int): Boolean {
        invalidateSelf()
        return true
    }

    @Deprecated("Deprecated in Java")
    override fun getOpacity(): Int = PixelFormat.TRANSLUCENT
}

